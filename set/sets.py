# Se crea un conjunto vacío llamado miset utilizando la función 'set()'
miset = set()

miset.add(1) # Se agrega el elemento 1 al conjunto utilizando
miset.add(2) # Se agrega el elemento 2 al conjunto utilizando
miset.add(3) # Se agrega el elemento 3 al conjunto utilizando

# Se imprime el conjunto completo utilizando:
print(miset)